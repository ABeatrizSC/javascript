/* Estruturas de repetição */

/* While */

var cont = 1

while (cont <= 5){
    console.log(`While: ${cont}`)
    cont++
}

/* Do While */

var cont = 1

do {
    console.log(`Do/While: ${cont}`)
    cont++
} while (cont <= 5)

/* For */

for (var cont = 1; cont <= 5; cont++){
    console.log(`For: ${cont}`)
}